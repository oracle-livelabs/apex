create or replace trigger crm_biu_lead_disqual_reason
    before insert or update of lead_state_id, lead_disqual_reason_id on crm_leads
    for each row
declare
    l_lead_state_code crm_lead_states.code%type;
begin
    select upper(code)
    into l_lead_state_code
    from crm_lead_states
    where lead_state_id = :new.lead_state_id;

    if l_lead_state_code = 'DISQUALIFIED'
       and :new.lead_disqual_reason_id is null then
        raise_application_error(
            -20001,
            'Disqualified leads must include a disqualification reason.'
        );
    end if;
end;
/

create or replace trigger crm_biu_opp_close_reason
    before insert or update of opportunity_stage_id, close_reason_id on crm_opportunities
    for each row
declare
    l_stage_code crm_opportunity_stages.code%type;
    l_outcome_type crm_opportunity_close_reasons.outcome_type%type;
begin
    select upper(code)
    into l_stage_code
    from crm_opportunity_stages
    where opportunity_stage_id = :new.opportunity_stage_id;

    if l_stage_code in ('CLOSED_WON', 'CLOSED_LOST') then
        if :new.close_reason_id is null then
            raise_application_error(
                -20002,
                'Closed opportunities must include a matching close reason.'
            );
        end if;

        select upper(outcome_type)
        into l_outcome_type
        from crm_opportunity_close_reasons
        where opportunity_close_reason_id = :new.close_reason_id;

        if l_stage_code = 'CLOSED_WON' and l_outcome_type <> 'WON' then
            raise_application_error(
                -20003,
                'Closed won opportunities require a close reason with outcome type WON.'
            );
        elsif l_stage_code = 'CLOSED_LOST' and l_outcome_type <> 'LOST' then
            raise_application_error(
                -20004,
                'Closed lost opportunities require a close reason with outcome type LOST.'
            );
        end if;
    end if;
end;
/

create or replace trigger crm_bu_order_require_bookings
    before update of order_status_id on crm_orders
    for each row
declare
    l_order_status_code crm_order_statuses.code%type;
    l_booking_count number;
begin
    select upper(code)
    into l_order_status_code
    from crm_order_statuses
    where order_status_id = :new.order_status_id;

    if l_order_status_code in ('CONFIRMED', 'DELIVERED')
       and nvl(:old.order_status_id, -1) <> :new.order_status_id then
        select count(*)
        into l_booking_count
        from crm_bookings
        where order_id = :new.order_id;

        if l_booking_count = 0 then
            raise_application_error(
                -20005,
                'Orders must include at least one booking before they can be confirmed or delivered.'
            );
        end if;
    end if;
end;
/

create or replace trigger crm_bu_order_require_approval
    before update of order_status_id, approval_status_id, approval_required on crm_orders
    for each row
declare
    l_order_status_code crm_order_statuses.code%type;
    l_approval_status_code crm_approval_statuses.code%type;
begin
    if :new.approval_required = 'Y' then
        select upper(code)
        into l_order_status_code
        from crm_order_statuses
        where order_status_id = :new.order_status_id;

        if l_order_status_code in ('CONFIRMED', 'DELIVERED') then
            if :new.approval_status_id is null then
                raise_application_error(
                    -20006,
                    'Approval-required orders must be approved before they can be confirmed or delivered.'
                );
            end if;

            select upper(code)
            into l_approval_status_code
            from crm_approval_statuses
            where approval_status_id = :new.approval_status_id;

            if l_approval_status_code <> 'APPROVED' then
                raise_application_error(
                    -20007,
                    'Approval-required orders must have approval status APPROVED before confirmation or delivery.'
                );
            end if;
        end if;
    end if;
end;
/

