create or replace view crm_vw_lead_funnel as
with lead_rollup as (
    select
        trunc(l.created_on, 'MM') as period_month,
        l.branch_id,
        b.name as branch_name,
        l.user_owner_id,
        u.username as owner_username,
        l.lead_source_id,
        ls.code as lead_source_code,
        count(*) as lead_count,
        sum(
            case
                when upper(st.code) = 'CONVERTED' or l.converted_opportunity_id is not null then 1
                else 0
            end
        ) as converted_lead_count,
        sum(case when upper(st.code) = 'DISQUALIFIED' then 1 else 0 end) as disqualified_lead_count,
        sum(
            case
                when upper(st.code) not in ('CONVERTED', 'DISQUALIFIED') then 1
                else 0
            end
        ) as open_lead_count,
        sum(nvl(l.expected_value_amt, 0)) as expected_value_amt,
        sum(case when l.converted_opportunity_id is not null then 1 else 0 end) as converted_opportunity_count,
        sum(case when l.converted_customer_id is not null then 1 else 0 end) as converted_customer_count
    from crm_leads l
    join crm_users u
        on u.user_id = l.user_owner_id
    join crm_lead_states st
        on st.lead_state_id = l.lead_state_id
    left join crm_branches b
        on b.branch_id = l.branch_id
    left join crm_lead_sources ls
        on ls.lead_source_id = l.lead_source_id
    group by
        trunc(l.created_on, 'MM'),
        l.branch_id,
        b.name,
        l.user_owner_id,
        u.username,
        l.lead_source_id,
        ls.code
)
select
    lr.period_month,
    lr.branch_id,
    lr.branch_name,
    lr.user_owner_id,
    lr.owner_username,
    lr.lead_source_id,
    lr.lead_source_code,
    lr.lead_count,
    lr.converted_lead_count,
    lr.disqualified_lead_count,
    lr.open_lead_count,
    lr.expected_value_amt,
    lr.converted_opportunity_count,
    lr.converted_customer_count,
    case
        when lr.lead_count > 0 then round(lr.converted_lead_count / lr.lead_count * 100, 2)
    end as conversion_rate_pct,
    case
        when lr.lead_count > 0 then round(lr.disqualified_lead_count / lr.lead_count * 100, 2)
    end as disqualification_rate_pct
from lead_rollup lr;

create or replace view crm_vw_opportunity_pipeline as
with opp_product_rollup as (
    select
        opportunity_id,
        count(*) as interested_product_count
    from crm_opportunity_products
    group by opportunity_id
)
select
    trunc(nvl(o.expected_close_date, o.created_on), 'MM') as period_month,
    nvl(a.branch_id, l.branch_id) as branch_id,
    b.name as branch_name,
    o.user_owner_id,
    u.username as owner_username,
    o.opportunity_stage_id,
    s.code as opportunity_stage_code,
    count(*) as opportunity_count,
    sum(nvl(o.value_amt, 0)) as pipeline_value_amt,
    sum(nvl(o.value_amt, 0) * nvl(o.probability_pct, 0) / 100) as weighted_pipeline_amt,
    avg(trunc(sysdate) - trunc(nvl(o.current_stage_date, o.created_on))) as avg_stage_age_days,
    sum(case when o.closed_date is not null then 1 else 0 end) as closed_opportunity_count,
    sum(
        case
            when o.closed_date is null
             and o.next_review_date is not null
             and trunc(o.next_review_date) < trunc(sysdate) then 1
            else 0
        end
    ) as overdue_review_count,
    sum(
        case
            when o.closed_date is null
             and trunc(sysdate) - trunc(nvl(o.current_stage_date, o.created_on)) > 30 then 1
            else 0
        end
    ) as stale_opportunity_count,
    sum(nvl(opr.interested_product_count, 0)) as interested_product_count
from crm_opportunities o
join crm_users u
    on u.user_id = o.user_owner_id
join crm_opportunity_stages s
    on s.opportunity_stage_id = o.opportunity_stage_id
left join crm_accounts a
    on a.account_id = o.account_id
left join crm_leads l
    on l.lead_id = o.lead_id
left join crm_branches b
    on b.branch_id = nvl(a.branch_id, l.branch_id)
left join opp_product_rollup opr
    on opr.opportunity_id = o.opportunity_id
group by
    trunc(nvl(o.expected_close_date, o.created_on), 'MM'),
    nvl(a.branch_id, l.branch_id),
    b.name,
    o.user_owner_id,
    u.username,
    o.opportunity_stage_id,
    s.code;

create or replace view crm_vw_opportunity_stage_duration as
with stage_entries as (
    select
        osh.opp_stage_hist_id,
        osh.opportunity_id,
        osh.new_stage_id as opportunity_stage_id,
        osh.changed_by_user_id,
        osh.changed_on as stage_start_on
    from crm_opportunity_stage_history osh
    where osh.new_stage_id is not null
    union all
    select
        cast(null as number) as opp_stage_hist_id,
        o.opportunity_id,
        o.opportunity_stage_id,
        o.user_owner_id as changed_by_user_id,
        nvl(o.current_stage_date, o.created_on) as stage_start_on
    from crm_opportunities o
    where not exists (
        select 1
        from crm_opportunity_stage_history osh
        where osh.opportunity_id = o.opportunity_id
          and osh.new_stage_id is not null
    )
),
ordered_entries as (
    select
        se.opp_stage_hist_id,
        se.opportunity_id,
        se.opportunity_stage_id,
        se.changed_by_user_id,
        se.stage_start_on,
        lead(se.stage_start_on) over (
            partition by se.opportunity_id
            order by se.stage_start_on, nvl(se.opp_stage_hist_id, 0)
        ) as next_stage_on
    from stage_entries se
)
select
    oe.opportunity_id,
    o.opportunity_name,
    nvl(a.branch_id, l.branch_id) as branch_id,
    b.name as branch_name,
    o.user_owner_id,
    ou.username as owner_username,
    oe.opportunity_stage_id,
    s.code as opportunity_stage_code,
    oe.stage_start_on,
    coalesce(oe.next_stage_on, o.closed_date, sysdate) as stage_end_on,
    round(coalesce(oe.next_stage_on, o.closed_date, sysdate) - oe.stage_start_on, 2) as duration_days,
    oe.changed_by_user_id,
    cu.username as changed_by_username
from ordered_entries oe
join crm_opportunities o
    on o.opportunity_id = oe.opportunity_id
join crm_opportunity_stages s
    on s.opportunity_stage_id = oe.opportunity_stage_id
join crm_users ou
    on ou.user_id = o.user_owner_id
left join crm_users cu
    on cu.user_id = oe.changed_by_user_id
left join crm_accounts a
    on a.account_id = o.account_id
left join crm_leads l
    on l.lead_id = o.lead_id
left join crm_branches b
    on b.branch_id = nvl(a.branch_id, l.branch_id);

