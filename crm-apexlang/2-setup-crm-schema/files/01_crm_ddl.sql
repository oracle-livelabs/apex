create or replace view crm_vw_forecast_accuracy as
with actuals as (
    select
        f.forecast_id,
        sum(
            case
                when upper(os.code) not in ('DRAFT', 'PENDING_APPROVAL', 'CANCELED')
                 and (
                    (f.forecast_scope_code = 'USER' and o.user_sales_id = f.user_id)
                    or (f.forecast_scope_code = 'BRANCH' and c.branch_id = f.branch_id)
                    or (f.forecast_scope_code = 'REGION' and cb.region_id = f.region_id)
                 )
                then nvl(o.total_amt, 0)
                else 0
            end
        ) as actual_amt,
        sum(
            case
                when upper(os.code) not in ('DRAFT', 'PENDING_APPROVAL', 'CANCELED')
                 and (
                    (f.forecast_scope_code = 'USER' and o.user_sales_id = f.user_id)
                    or (f.forecast_scope_code = 'BRANCH' and c.branch_id = f.branch_id)
                    or (f.forecast_scope_code = 'REGION' and cb.region_id = f.region_id)
                 )
                then 1
                else 0
            end
        ) as actual_order_count
    from crm_forecasts f
    left join crm_orders o
        on o.order_date between f.period_start_date and f.period_end_date
    left join crm_order_statuses os
        on os.order_status_id = o.order_status_id
    left join crm_customers c
        on c.customer_id = o.customer_id
    left join crm_branches cb
        on cb.branch_id = c.branch_id
    group by f.forecast_id
)
select
    f.forecast_id,
    f.forecast_category_id,
    fc.code as forecast_category_code,
    f.forecast_scope_code,
    f.user_id,
    u.username as forecast_username,
    f.branch_id,
    b.name as forecast_branch_name,
    f.region_id,
    r.name as forecast_region_name,
    f.period_start_date,
    f.period_end_date,
    f.snapshot_date,
    f.forecast_amt,
    nvl(a.actual_amt, 0) as actual_amt,
    nvl(a.actual_order_count, 0) as actual_order_count,
    nvl(a.actual_amt, 0) - nvl(f.forecast_amt, 0) as variance_amt,
    abs(nvl(a.actual_amt, 0) - nvl(f.forecast_amt, 0)) as abs_variance_amt,
    case
        when nvl(f.forecast_amt, 0) <> 0 then round((nvl(a.actual_amt, 0) - f.forecast_amt) / f.forecast_amt * 100, 2)
    end as variance_pct,
    case
        when nvl(a.actual_amt, 0) = 0 and nvl(f.forecast_amt, 0) = 0 then 100
        when greatest(abs(nvl(a.actual_amt, 0)), abs(nvl(f.forecast_amt, 0))) > 0 then
            round(
                (
                    1 - (
                        abs(nvl(a.actual_amt, 0) - nvl(f.forecast_amt, 0))
                        / greatest(abs(nvl(a.actual_amt, 0)), abs(nvl(f.forecast_amt, 0)))
                    )
                ) * 100,
                2
            )
    end as accuracy_pct
from crm_forecasts f
left join actuals a
    on a.forecast_id = f.forecast_id
left join crm_forecast_categories fc
    on fc.forecast_category_id = f.forecast_category_id
left join crm_users u
    on u.user_id = f.user_id
left join crm_branches b
    on b.branch_id = f.branch_id
left join crm_regions r
    on r.region_id = f.region_id;

