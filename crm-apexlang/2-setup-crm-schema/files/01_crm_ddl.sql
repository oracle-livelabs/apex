create or replace view crm_vw_quote_conversion as
with quote_orders as (
    select
        quote_id,
        count(*) as order_count,
        sum(nvl(total_amt, 0)) as order_total_amt
    from crm_orders
    where quote_id is not null
    group by quote_id
)
,
quote_rollup as (
    select
        trunc(q.quote_date, 'MM') as period_month,
        a.branch_id,
        b.name as branch_name,
        q.user_owner_id,
        u.username as owner_username,
        q.quote_status_id,
        qs.code as quote_status_code,
        count(*) as quote_count,
        sum(case when q.accepted_date is not null then 1 else 0 end) as accepted_quote_count,
        sum(case when q.rejected_date is not null then 1 else 0 end) as rejected_quote_count,
        sum(case when nvl(qo.order_count, 0) > 0 then 1 else 0 end) as converted_quote_count,
        sum(nvl(qo.order_count, 0)) as converted_order_count,
