        avg(
            case
                when q.accepted_date is not null then q.accepted_date - q.quote_date
                when q.rejected_date is not null then q.rejected_date - q.quote_date
                when q.expiry_date is not null
                 and q.expiry_date <= trunc(sysdate)
                 and q.accepted_date is null
                 and q.rejected_date is null then q.expiry_date - q.quote_date
            end
        ) as avg_turnaround_days
    from crm_quotes q
    join crm_users u
        on u.user_id = q.user_owner_id
    join crm_quote_statuses qs
        on qs.quote_status_id = q.quote_status_id
    left join crm_accounts a
        on a.account_id = q.account_id
    left join crm_branches b
        on b.branch_id = a.branch_id
    left join quote_orders qo
        on qo.quote_id = q.quote_id
    group by
        trunc(q.quote_date, 'MM'),
        a.branch_id,
        b.name,
        q.user_owner_id,
        u.username,
        q.quote_status_id,
        qs.code
)
select
    qr.period_month,
    qr.branch_id,
    qr.branch_name,
    qr.user_owner_id,
    qr.owner_username,
    qr.quote_status_id,
    qr.quote_status_code,
    qr.quote_count,
    qr.accepted_quote_count,
    qr.rejected_quote_count,
    qr.converted_quote_count,
    qr.converted_order_count,
    qr.converted_order_amt,
    qr.total_quote_amt,
    qr.total_discount_amt,
    qr.avg_discount_amt,
    qr.avg_turnaround_days,
    case
        when qr.quote_count > 0 then round(qr.accepted_quote_count / qr.quote_count * 100, 2)
    end as acceptance_rate_pct,
    case
        when qr.quote_count > 0 then round(qr.converted_quote_count / qr.quote_count * 100, 2)
    end as order_conversion_rate_pct
from quote_rollup qr;

